package Model;

/**
 *
 * @author root
 */
public class UserModel {
    private String username;
    private String pass;
    private String id;

    public UserModel() {
    }

    public UserModel(String username, String pass, String id) {
        this.username = username;
        this.pass = pass;
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
    
}
